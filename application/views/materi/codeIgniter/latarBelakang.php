<h2>Code Igniter</h2>
<p>
	Codeigniter adalah satu dari sekian banyak framework yang biasa digunakan oleh para developer PHP. Jenis framework
	ini bisa dibilang paling banyak digunakan di antara jenis framework lainnya, karena mampu mempercepat pengembangan
	web berbasis PHP berdasarkan package serta array library yang lebih luas.
	Selain itu, Framework codeigniter juga terkenal ringan karena sistem utama framework ini hanya membutuhkan beberapa
	library kecil sehingga bisa menghemat resource website.
	Alhasil, Anda bisa lebih leluasa dalam mengembangkan website dengan kualitas terbaik tanpa harus khawatir bakal
	memakan resource besar pada hosting yang Anda gunakan.
	Framework Codeigniter pertama kali diperkenalkan pada tanggal 28 Febrari 2006.
</p>
