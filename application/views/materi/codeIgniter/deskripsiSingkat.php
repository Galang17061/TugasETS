	<h2>Penjelasan Singkat</h2>
	<p>
		CodeIgniter adalah sebuah web application network yang bersifat open source yang digunakan untuk membangun aplikasi
		php dinamis.
		CodeIgniter menjadi sebuah framework PHP dengan model MVC (Model, View, Controller) untuk membangun website dinamis
		dengan menggunakan PHP yang dapat mempercepat pengembang untuk membuat sebuah aplikasi web. Selain ringan dan
		cepat, CodeIgniter juga memiliki dokumentasi yang super lengkap disertai dengan contoh implementasi kodenya.
		Dokumentasi yang lengkap inilah yang menjadi salah satu alasan kuat mengapa banyak orang memilih CodeIgniter
		sebagai framework pilihannya. Karena kelebihan-kelebihan yang dimiliki oleh CodeIgniter, pembuat PHP Rasmus Lerdorf
		memuji CodeIgniter di frOSCon (Agustus 2008) dengan mengatakan bahwa dia menyukai CodeIgniter karena “it is faster,
		lighter and the least like a framework.”
		CodeIgniter pertamakali dikembangkan pada tahun 2006 oleh Rick Ellis. Dengan logo api yang menyala, CodeIgniter
		dengan cepat “membakar” semangat para web developer untuk mengembangkan web dinamis dengan cepat dan mudah
		menggunakan framework PHP yang satu ini.
	</p>
