<h2>Tujuan Code Igniter</h2>

<p>
	<ul>
		<li>
			<p>
				Aplikasi yang dibuat menggunakan codeigniter bisa bejalan cepat.
			</p>
		</li>
		<li>
			<p>
				Codeigniter terdokumentasi dengan baik informasi tentang pustaka kelas (Library) dan fungsi yang
				disediakan oleh codeigniter dapat diperoleh melalui dokumentasi yang disertakan di dalam paket
				distribusinya.
			</p>
		</li>
		<li>
			<p>
				Codeigniter mengizinkan pengembang web menggunakan library atau helper yang tidak disediakan oleh
				codeigniter seperti: Google Map API, Facebook API, fpdf, dan lain sebagainya.
			</p>
		</li>
		<li>
			<p>
				Codeigniter mendukung banyak RDBMS (Relational Database Management System) seperti MySQL, MySQLi, SQL
				Server, Oracle, Maria DB, PostgreSQL, SQLite, dan lain sebagainya.
			</p>
		</li>
		<li>
			<p>
				Codeigniter pada dasarnya menganut Clean URL dan mendukung SEO (Search Engine Optimazation). Sehingga
				setiap aplikasi yang dibangun menggunakan codeigniter lebih mudah di index oleh search engine populer
				seperti google, yahoo, msn, dan lain sebagainya.
			</p>
		</li>
	</ul>
</p>
