<h2>Struktur Laravel</h2>
<p>
	MVC adalah perangkat lunak yang memisahkan aplikasi logika dari presentasi. MVC memisahkan aplikasi berdasrkan
	komponen-komponen aplikasi seperti manipulasi data, controller, dan user interface.
	<ul>
		<li>
			<p>
				Model, Model mewakili struktur data. Biasanya model berisi fungsi-fungsi yang membantu seseorang dalam
				pengelolaan
				basis data seperti memasukkan data ke basis data, pembaruan data dan lain-lain.
			</p>
		</li>
		<li>
			<p>
				View, View adalah bagian yang mengatur tampilan ke pengguna. Bisa dikatakan berupa halaman web.
			</p>
		</li>
		<li>
			<p>
				Controller, Controller merupakan bagian yang menjebatani model dan view.
			</p>
		</li>
	</ul>
</p>
