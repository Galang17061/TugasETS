<h2>Fitur Pada Laravel</h2>
<p>
	<ul>
		<li>
			<p>Bundles, yaitu sebuah fitur dengan sistem pengemasan modular dan tersedia beragam di aplikasi.</p>
		</li>
		<li>
			<p>
				Eloquent ORM, merupakan penerapan PHP lanjutan menyediakan metode internal dari pola “active record”
				yang
				menagatasi masalah pada hubungan objek database.
			</p>
		</li>
		<li>
			<p>Application Logic, merupakan bagian dari aplikasi, menggunakan controller atau bagian Route.</p>
		</li>
		<li>
			<p>Reverse Routing, mendefinisikan relasi atau hubungan antara Link dan Route.</p>
		</li>
		<li>
			<p>Restful controllers, memisahkan logika dalam melayani HTTP GET and POST.</p>
		</li>
		<li>
			<p>Class Auto Loading, menyediakan loading otomatis untuk class PHP.</p>
		</li>
		<li>
			<p>View Composer, adalah kode unit logikal yang dapat dieksekusi ketika view sedang loading.</p>
		</li>
		<li>
			<p>IoC Container, memungkin obyek baru dihasilkan dengan pembalikan controller.</p>
		</li>
		<li>
			<p>Migration, menyediakan sistem kontrol untuk skema database.</p>
		</li>
		<li>
			<p>Unit Testing, banyak tes untuk mendeteksi dan mencegah regresi.</p>
		</li>
		<li>
			<p>Automatic Pagination, menyederhanakan tugas dari penerapan halaman.</p>
		</li>
	</ul>
</p>
