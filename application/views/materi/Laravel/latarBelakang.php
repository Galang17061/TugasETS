<h2>Latar Belakang</h2>
<p>Kemunculan framework bisa dikatakan mirip dengan kondisi partai di Indonesia, setiap musim selalu saja ada yang
	baru, dengan ideologi yang sebenarnya ga beda-beda jauh dari yang sudah ada. Kalau bukan performa yang sangat
	cepat, pasti yang diandalkan adalah kecepatan development dimana berbagai macam kebutuhan standard seperti
	autentikasi, form validation, dan query ke database sudah disediakan out of the box.
	Framework PHP = partai di Indonesia, kebanyakan, ideologinya mirip-mirip
	Nah, sejak tahun 2012 muncul satu fenomena yang cukup berbeda dan menarik perhatian, dimana ada satu framework
	yang membawa ideologi baru yang selama ini jarang diperhatikan, yaitu aspek “clean code” dan “expressiveness”.
	Framework ini mengaku “clean and classy”, kodenya lebih singkat, mudah dimengerti, dan ekspressif, jadi hanya
	dengan membaca sekilas kode yang ditulis Anda sudah bisa menduga apa maksudnya tanpa perlu membaca dokumentasi.
	Framework ini dinamakan LARAVEL.
</p>
