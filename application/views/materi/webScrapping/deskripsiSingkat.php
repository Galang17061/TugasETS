	<h2>Apa itu Web Scraping?</h2>
	<p>Web scraping adalah proses ekstrasi data dari sebuah website. Salah satu contoh web scraping adalah meng-copy
		daftar contact dari sebuah direktori web. Memang Anda bisa saja melakukan ini secara manual denagn meng-copy
		paste data ke excel, misalnya. Tetapi bagaimana kalau datanya banyak? Untuk ini, Anda membutuhkan automation
		yang bisa membantu proses web scraping Anda lebih cepat dan mudah.

		Web scraping dilakukan dengan menggunakan web scraper, bot, web spider, atau web crawler. Web scraper sendiri
		adalah program yang masuk ke halaman website, download kontennya, mengekstrak data dari konten, dan menyimpan
		data ke satu file atau database.
	</p>

