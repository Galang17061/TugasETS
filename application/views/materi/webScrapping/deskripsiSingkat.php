	<h2>Kenapa kita perlu melakukan web scraping?</h2>
	<p>Ada banyak alasan mengapa web scraping semakin diperlukan di zaman sekarang. Dengan semakin berkembangnya big
		data, jumlah data yang tersedia sudah tidak terhitung lagi. Bayangkan kalau Anda harus mengumpulkan dan
		menyimpan jutaan data dalam satu file sendirian, pasti pusing kan? Web scraping bisa membantu Anda untuk
		mengumpulkan data dengan lebih cepat. Selain itu, kalau memang data yang Anda kumpulkan berjumlah besar, Anda
		juga bisa melakukan automation dan Anda tidak perlu repot lagi karena yang penting Anda bisa membiarkan server
		Anda berjalan.

		Dengan efisiensi web scraping, ini juga membantu proses analisa data Anda. Karena web scraping membantu
		mengumpulkan semua data tanpa ketinggalan, Anda akan mendapat data lengkap dari proses ini. Dengan begitu, Anda
		bisa mencari tahu lebih banyak tentang demografis Anda, mulai dari gender, umur, dan data-data lain yang bisa
		membantu bisnis Anda. Data-data ini tentunya akan memberi insight yang bernilai untuk membantu Anda membuat
		keputusan yang tepat dalam berbisnis.
