	<h2>Teknik-teknik web scraping</h2>
	<p>Dengan semakin banyaknya orang yang melakukan web scraping, ada beberapa teknik automasi yang bisa Anda lakukan
		untuk melakukan web scraping.
	</p>

	<div class="line"></div>

	<h2>Parsing HTML</h2>
	<p>Parsing HTML adalah salah satu teknik yang paling banyak digunakan dalam web parsing. Biasanya parsing HTML
		dilakukan melalui JavaScript dan menarget halaman HTML linear dan nested. Metode yang cepat ini mengidentifikasi
		script HTML dari website, yang mungkin saja dilakukan secara manual sebelumnya. Script ini kemudian digunakan
		untuk mengekstraksi text, links, dan data.</p>
	<div class="line"></div>

	<h2>Parsing DOM</h2>
	<p>Konten,style, dan struktur file XML didefinisikan dalam DOM, kependekan dari Document Object Model. Scrapers yang
		ingin mengetahui cara kerja internal halaman web dan mengekstrak skrip yang berjalan di dalamnya biasa memilih
		untuk melakukan web scraping melalui parsing DOM. Node spesifik dikumpulkan menggunakan parser DOM dan alat-alat
		seperti XPath membantu proses scraping sebuah halaman web.</p>
	<div class="line"></div>

	<h2>XPath</h2>
	<p>XML Path Language atau lebih dikenal dengan istilah XPath adalah bahasa query yang bekerja di dokumen XML. Karena
		dokumen XML biasa disusun dengan struktur pohon (tree structure), XPath bisa digunakan untuk menavigasi struktur
		dokumen tersebut dengan memilih nodes berdasarkan berbagai parameter. XPath juga bisa digunakan bersamaan dengan
		DOM parsing dalam mengesktrasi seluruh halaman website dan menampilkannya di website lain.</p>
	<div class="line"></div>
	<h2>Google Docs</h2>
	<p>Google Sheets juga ternyata bisa digunakan sebagai alat scraping, loh! Malahan ini adalah salah satu alat
		scraping yang popular. Di Google Sheets sendiri, Anda bisa memanfaatkan fungsi IMPORTXML untuk melakukan
		scraping data dari website. Selain itu, Anda juga bisa menggunakan command ini untuk melihat apakah website Anda
		aman dari scraping.</p>
