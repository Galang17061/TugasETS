<h2>Kegunaan</h2>
<p>Aplikasi yang dibangun memanfaatkan teknologi web
	scraping untuk mengambil data pada berbagai situs
	berita. Data berita yang diambil tersebut langsung
	masuk ke dalam database untuk disimpan. Data berita
	dalam database dapat ditampilkan pada aplikasi. Selain
	ditampilkan, data berita dapat diolah untuk di ringkas
	menjadi lebih mudah mengetahui inti yang dicari.
	Aplikasi untuk meringkas berita memanfaatkan metode TFIDF.
	TF-IDF digunakan karena dapat memberikan nilai
	pada kalimat yang sebagai inti pada sebuah berita.
	Aplikasi untuk meringkas dan menampilkan berita dari
	berbagai sumber berjalan pada mobile device. Aplikasi
	mobile tersebut digunakan oleh pengguna secara umum.
	Pada sisi administrator akan mengatur scraping website
	yang berisi pengaturan URL(Uniform Resource Locator)
	dan rules.
	Dengan adanya aplikasi ini memudahkan pengguna
	dalam membaca berita. Pengguna tidak harus melihat
	berita per situs atau mengunduh semua aplikasi berita,
	hanya dengan satu aplikasi dapat melihat semua berita
	dari berbagai sumber. Selain dapat melihat berita dari
	berbagai sumber, ada fitur peringkas berita yang akan
	memudahkan pengguna dalam mengetahui inti berita yang
	dicari.</p>
