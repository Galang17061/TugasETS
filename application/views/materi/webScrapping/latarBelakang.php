	<h2>Latar Belakang</h2>
	<p>Seiring dengan perkembangan teknologi informasi
		yang maju seperti sekarang ini membuat orang semakin
		cepat dalam mengakses informasi. Informasi bisa
		didapatkan lewat internet atau online. Informasi yang
		paling sering di akses adalah berita. Hal tersebut
		diimbangi dengan penyedia layanan situs berita online
		di Indonesia yang semakin banyak. Banyak media cetak
		maupun televisi sekarang sudah mempunyai situs berita
		online sendiri.
		Situs berita online memudahkan pengguna dalam
		harus berpindah–pindah situs berita untuk melihat
		berita yang bagus dan berbobot. Apalagi sekarang
		penyedia situs berita online tersebut mempunyai
		aplikasi mobile pembaca berita sendiri–sendiri. Dengan
		keadaan tersebut membuat pembaca berita harus memasang
		aplikasi lebih dari satu kalau ingin membaca berita
		dari berbagai sumber. Saat ini dibutuhkan aplikasi
		khusus untuk menggabungkan beberapa situs tersebut
		menjadi satu. Teknik web scraping tersebut yang cocok
		membaca berita dimanapun dan kapanpun. Kebanyakan
		informasi yang ada di internet banyak yang bertele–tele
		dan banyak informasi yang tidak penting masuk di dalam
		berita tersebut (Putra, 2014). Sedangkan konten yang
		bagus dan unik dapat menarik banyak pengunjung website,
		itu merupakan kunci utama website atau situs online
		tersebut populer (Hardiono, 2010).
	</p>
