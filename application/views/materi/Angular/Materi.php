<h2>Aturan Pada Angular</h2>
<p>
	<ul>
		<li>
			<p>
				Satu file memiliki satu jenis fungsi
			</p>
		</li>
		<li>
			<p>
				Jumlah line of code pada satu file maksimal 400 line
			</p>
		</li>
		<li>
			<p>
				Menuliskan fungsi(max 75 line)
			</p>
		</li>
		<li>
			<p>
				Menggunakan nama konsisten. Misalnya untuk file services maka dituliskan nama_pipe.pipe untuk model maka
				dituliskan nama_mode.model dan lain-lain
			</p>
		</li>
		<li>
			<p>
				Class name selalu ditulis diawali dengan UPPERCASE.
			</p>
		</li>
		<li>
			<p>
				Gunakan suffix “service” dan prefix sebagai nama servicenya. Contoh : NoteService.
			</p>
		</li>
		<li>
			<p>
				Jangan gunakan underscore untuk mendefinisikan properti/method/funciton.
			</p>
		</li>
		<li>
			<p>
				Saat melakukan import berikan space pada kiri dan kanan bracket
			</p>
		</li>
	</ul>
</p>
